    SELECT ((string_to_array(val, ' '))[2])::int as qty
          ,((string_to_array(val, ' '))[4])::int as src
          ,((string_to_array(val, ' '))[6])::int as dest
          ,row_number() over() as move_id
      FROM input
     WHERE val ~ 'move'
),
compute AS (
    SELECT crate
          ,row_id
          ,col_id
          ,0::bigint as move_id
      FROM parsing
     WHERE NULLIF(trim(crate), '') IS NOT NULL

UNION ALL

    (WITH src AS (
        SELECT c.crate
              ,c.row_id
              ,c.col_id
              ,m.move_id
              ,m.qty
              ,m.src
              ,m.dest
          FROM compute c
          JOIN moves m ON m.move_id=c.move_id+1
    ),
    limitd AS (
        SELECT COALESCE(max(s.row_id), 0) as max_row_dest
          FROM src s
         WHERE col_id=dest
    ),
    limits AS(
        SELECT max(s.row_id) as max_row_src
              ,max(s.row_id-qty+1) as min_row_src
          FROM src s
         WHERE col_id=src
    ),
    cpt AS (
        SELECT s.crate
            ,s.row_id
            ,s.col_id
            ,s.move_id
        FROM src s
        JOIN limits as ls ON TRUE
        WHERE s.col_id!=s.src
            OR s.row_id<ls.min_row_src
        
        UNION ALL

        SELECT s.crate
            ,ld.max_row_dest+row_number() over(order by row_id desc) as row_id
            ,s.dest as col_id
            ,s.move_id
        FROM src s
        JOIN limitd as ld ON TRUE
        JOIN limits as ls ON TRUE
        WHERE s.col_id=s.src
        AND s.row_id>=ls.min_row_src
    )
    SELECT * FROM cpt)
),
cpt_limit AS (
    SELECT max(row_id) as row_id
          ,col_id
          ,m.move_id as move_id
      FROM compute c
